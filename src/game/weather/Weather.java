package game.weather;

/**
 * An enumeration representing different weather conditions in the game.
 *
 * @author Jerry
 */
public enum Weather {
    /**
     * Represents a sunny weather condition.
     */
    SUNNY,

    /**
     * Represents a rainy weather condition.
     */
    RAINY,

    /**
     * Represents a default weather condition.
     */
    DEFAULT,
}
