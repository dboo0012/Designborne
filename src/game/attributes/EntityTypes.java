package game.attributes;

public enum EntityTypes {
    /**
     * Indicates that the actor is a player actor.
     */
    PLAYABLE,
    /**
     * Indicates that the actor is an enemy.
     */
    ENEMY,
    /**
     * Indicates that the actor is a trader.
     */
    TRADER,
    /**
     * Indicates that the actor is immune to the void.
     */
    BOSS,
}
